import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner scanner = new Scanner(System.in);
        int X = scanner.nextInt();
        
        if (X >= 8 && X <= 10) {
            System.out.println("YES");
        } else {
            System.out.println("NO");
        }
        
        scanner.close();

	}
}
